with b as (
select "Адрес" ~ '\*' "OSM",
       "Адрес" !~ '\*' "План 1978",
       split_part("Адрес", '×', 1)::int2 "Уч.",
       replace(split_part("Адрес", '×', 2), '*', '') "№",
