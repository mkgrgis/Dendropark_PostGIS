       "План 1978",
       "Уч.",
       "№",
       "№доп",
       "Флаг",
       regexp_replace("Вид или род", '\^|\*', '') "Вид или род",
       "Вид или род" ~ '\*' "Утрата",
       "Вид или род" ~ '\^' "После 2020"
from b
order by "Уч." asc, "№", "№доп";

